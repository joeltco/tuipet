    public static void loadConfig(int setting, String modFolder, boolean tournament) {
        modFolder = tournament ? "\\/:*?\"<>|" : modFolder + "csv" + File.separator;
        try (InputStream in = Utility.getInputStream(modFolder, "/Model/", "config.csv");
             BufferedReader reader = new BufferedReader(new InputStreamReader(in, "utf-8"));){
            ArrayList<String> info = new ArrayList<String>();
            String line = reader.readLine();
            while (line != null) {
                String[] detail = line.split(",");
                info.add(detail[1] + "," + detail[2] + "," + detail[3]);
                line = reader.readLine();
            }
            Config.readConfigInfo(info.toArray(new String[info.size()]), setting);
        }
        catch (Exception e) {
            e.printStackTrace();
        }
    }

    private static void writeConfig(String[] labels, String[] info) {
        try (PrintWriter save = new PrintWriter("config.csv");){
            for (int i = 0; i < labels.length; ++i) {
                save.println(labels[i] + "," + info[i]);
            }
        }
        catch (Exception e) {
            e.printStackTrace();
        }
    }

