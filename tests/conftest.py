@pytest.fixture(autouse=True)
def _sandbox_lobby_feed(tmp_path, monkeypatch):
    """The lobby server module logs public events to FEED_PATH on login/chat/
    disconnect -- any test driving server flows must write a scratch feed, or
    pytest runs append real-looking events to the repo copy (leak found
    2026-07-10: test_bugreport/test_save_integrity grew server/lobby_feed.jsonl
    on every run)."""
    import os as _os
    import sys as _sys
    # import (not just look up) the module: the first test in a session that
    # imports server INSIDE its body used to run before this fixture had
    # anything to patch -- order-dependent sandboxing (test audit 2026-07-13)
    _sys.path.insert(0, _os.path.join(_os.path.dirname(__file__), "..", "server"))
    try:
        import server as srv
    except Exception:
        srv = None
    if srv is not None and hasattr(srv, "FEED_PATH"):
        monkeypatch.setattr(srv, "FEED_PATH", str(tmp_path / "lobby_feed.jsonl"))
    if srv is not None and hasattr(srv, "BUGS_PATH"):
        monkeypatch.setattr(srv, "BUGS_PATH", str(tmp_path / "bugs.jsonl"))
    if srv is not None and hasattr(srv, "LADDER_PATH"):
        monkeypatch.setattr(srv, "LADDER_PATH", str(tmp_path / "ladder.json"))
        monkeypatch.setattr(srv, "LADDER", {"seasons": {}, "claimed": {}})
        srv._ladder_pending.clear()
        srv._ladder_confirm.clear()
        srv._ladder_pair_hour.clear()

@pytest.fixture(autouse=True)
def never_run_pip(monkeypatch):
    """No test may install anything.  The launch auto-updater (2026-07-14) calls
    update.run_upgrade(), and a test that forgot to mock it REALLY RAN pip --
    it upgraded tuipet inside .venv-dev mid-suite.  Same doctrine as
    isolate_save: the suite can never touch the real thing.  A test that wants
    the success path monkeypatches run_upgrade itself; this only stops the real
    one.
    """
    from tuipet import update

    def _refuse(*_a, **_k):
        raise AssertionError(
            "a test tried to shell out to pip -- patch update._RUN "
            "(or mock run_upgrade/upgrade_argv) instead")

    monkeypatch.setattr(update, "_RUN", _refuse)
